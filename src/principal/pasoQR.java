    private void cargarPagos(){
    DefaultTableModel modelo = (DefaultTableModel) tablaPagos.getModel();
    modelo.setRowCount(0); // limpiar tabla

    try(Connection con = conexion.getConnection();
        Statement st = con.createStatement();
        ResultSet rs = st.executeQuery("SELECT * FROM pago")){
        while(rs.next()){
            modelo.addRow(new Object[]{
                rs.getInt("id_pago"),
                rs.getString("nombre_cliente"),
                rs.getDouble("monto"),
                rs.getString("estado")
            });
        }
    } catch(SQLException e){
        e.printStackTrace();
    }
}

private void generarQR(int id, String nombre, double monto){
    String datos = "id_pago="+id+"\nNombre:"+nombre+"\nMonto:"+monto;
    try{
        QRCodeWriter qr = new QRCodeWriter();
        BitMatrix bm = qr.encode(datos, BarcodeFormat.QR_CODE, 200, 200);
        Path path = Path.of("pagoQR.png");
        MatrixToImageWriter.writeToPath(bm, "PNG", path);
        lblQR.setIcon(new ImageIcon(path.toAbsolutePath().toString()));
        JOptionPane.showMessageDialog(this, "QR generado para el pago ID "+id);
    } catch(Exception e){
        e.printStackTrace();
    }
}

private void validarPago(int id){
    try(Connection con = conexion.getConnection();
        PreparedStatement ps = con.prepareStatement("UPDATE pago SET estado='PAGADO' WHERE id_pago=?")){
        ps.setInt(1, id);
        int res = ps.executeUpdate();
        if(res>0){
            JOptionPane.showMessageDialog(this, "Pago ID "+id+" validado correctamente.");
        }
    } catch(SQLException e){
        e.printStackTrace();
    }
}


