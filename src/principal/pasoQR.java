    private void btnGenerarQRActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarQRActionPerformed
        // TODO add your handling code here:
        int fila = tablaPagos.getSelectedRow();
        if(fila != -1){
            int idPago = Integer.parseInt(tablaPagos.getValueAt(fila, 0).toString());
            String nombre = tablaPagos.getValueAt(fila, 1).toString();
            double monto = Double.parseDouble(tablaPagos.getValueAt(fila,2).toString());
            generarQR(idPago, nombre, monto);
        } else {
            JOptionPane.showMessageDialog(this, "Seleccione un pago de la tabla.");
        }
    }//GEN-LAST:event_btnGenerarQRActionPerformed

